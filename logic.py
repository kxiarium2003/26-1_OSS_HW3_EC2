def calculate_detailed_report(score, total):
    """
    사용자의 정답 개수를 바탕으로 정답률을 계산하여 
    데이터 사이언스 역량 분석 리포트를 생성합니다.
    """
    # 정답률 계산 (예: 5문제 중 3문제면 60.0)
    ratio = (score / total) * 100
    
    if ratio == 100:
        return f"""
        ### 🏆 [분석 결과: Fintech Master]
        **정답률 {ratio}% - 완벽한 도메인 이해도**
        
        {score}문제를 모두 맞히셨습니다! 데이터 분석 역량과 금융 지식이 완벽한 조화를 이루고 있습니다. 
        차세대 핀테크 엔지니어로서 손색없는 수준이며, 특히 복잡한 금융 데이터의 흐름과 통계적 접근 방식을 정확히 이해하고 계시네요. 
        현업 수준의 프로젝트에 바로 투입되어도 충분한 역량입니다!
        """
        
    elif ratio >= 60:
        return f"""
        ### 📈 [분석 결과: Data-Driven Learner]
        **정답률 {ratio}% - 우수한 성장 잠재력**
        
        {total}문제 중 {score}문제를 맞히셨습니다. 금융 지식의 데이터적 해석 능력이 상당히 우수합니다. 
        기본적인 메커니즘은 잘 파악하고 있으나, 최신 핀테크 기술 스택이나 더 복잡한 시계열 분석 기법에 대해 조금 더 심화 학습을 진행한다면 
        훌륭한 데이터 전문가로 거듭날 수 있을 것입니다.
        """
        
    else:
        return f"""
        ### 🔍 [분석 결과: Emerging Explorer]
        **정답률 {ratio}% - 기초 탐색 단계**
        
        현재 핀테크 도메인에 대한 기초를 다지는 단계입니다. 
        데이터 사이언스 기술을 금융 현장에 실제 적용하기 위해서는 금융 용어의 정의와 시장의 흐름을 먼저 익히는 것이 중요합니다. 
        포기하지 말고 전공 지식을 도메인에 접목하는 연습을 꾸준히 해보시길 권장합니다. 화이팅!
        """